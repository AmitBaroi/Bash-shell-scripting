#!/bin/bash

# Simple script that greets the world of Bash!
# Usage: ./hello


# The echo command is just like the print command in Python
# Unlike Python however, we dont need to write strings in quotes
# We can just write them seperated by spaces as seperate arguments to the command
# echo will automatically add a whitespace between the args Hello and World!
# Outputting: Hello World!
echo Hello World!


# Its always helpful to specify the exit code
# In future Lessons we will see how to conditionally return different exit codes
exit 0